package eeIteration;

public class aaFor {
	public static void main(String[] args){
		System.out.println("BARIS MAJU");
		for (int i = 1; i <= 5; i++) {
			System.out.println("Angka ke : "+i);
		}
		
		System.out.println("BARIS MUNDUR");
		for (int j = 5; j >= 1 ; j--) {
			System.out.println("Angka ke : "+j);
		}
	}
}
